public class Mountain {

    private int axeV;
    private int axeH;

    public Mountain(int axeH, int axeV) {
        this.axeH = axeH;
        this.axeV = axeV;
    }

    public int getAxeV() {
        return axeV;
    }

    public void setAxeV(int axeV) {
        this.axeV = axeV;
    }

    public int getAxeH() {
        return axeH;
    }

    public void setAxeH(int axeH) {
        this.axeH = axeH;
    }
}
